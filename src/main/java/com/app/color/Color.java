package com.app.color;

/**
 * An enumeration representing various colors.
 * This enum provides a predefined set of color constants.
 */
public enum Color {
    /** The color white. */
    WHITE,

    /** The color gray. */
    GRAY,

    /** The color black. */
    BLACK,

    /** The color silver. */
    SILVER,

    /** The color blue. */
    BLUE,

    /** The color red. */
    RED,

    /** The color green. */
    GREEN,

    /** The color brown. */
    BROWN,

    /** The color orange. */
    ORANGE,

    /** The color yellow. */
    YELLOW,

    /** The color gold. */
    GOLD,

    /** The color purple. */
    PURPLE
}